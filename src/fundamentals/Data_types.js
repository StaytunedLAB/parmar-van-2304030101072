/*JavaScript Data types*/


// Number Data type
// ➡️ number data type is used to represent both integer and floating-point numbers in JavaScript.
let age = 25;
console.log(age);

// BigInt Data type
const bigInteger = 16465498784878449845187468789n; // BigInt data type
console.log(bigInteger);

// String Data type
let name = "Utkarsh Prajapati";
console.log(name);

let user = 'Utkarsh';

alert(`hello ${user}`); // Template literal

// Boolean Data type
let isGreater = 5 > 3;
console.log(isGreater); // it gives true 

// null Data type
let data = null;
console.log(data); // it gives null 

// undefined Data type 
let year = 2025;
year = undefined;
console.log(year); // it gives undefined

// tyoeof operator
console.log(typeof age); // number